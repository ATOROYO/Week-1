// /🌟 Control Flow in Dart / ////
void main() {
  // 🌟 Control Flow in Dart
  var age = 10;

  if (age > 18) {
    print("🗳️ Sarah is a voter in Uganda!");
  }
  print("👶🏽 Sarah is still too young to vote.");

  // If-else Statements
  var age2 = 10;

  if (age2 > 18) {
    print("🗳️ Sarah is a voter in Uganda!");
  } else {
    print("👶🏽 Sarah is still too young to vote.");
  }
}
